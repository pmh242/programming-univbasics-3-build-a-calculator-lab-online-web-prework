def addition(num1, num2)
  sum = num2 + num1
  puts sum
end

def subtraction(num1, num2)
  minus = num2 - num1
  puts = minus
end

def division(num1, num2)
  divide = num2 // num1
  puts divide
end

def multiplication(num1, num2)
  multiple = num1 * num2
  puts multiple
end

def modulo(num1, num2)
  mod = num2 % num1
  puts mod
end

def square_root(num)
  sqft = num
  Math.sqrt(sqft)
  puts sqft
end
